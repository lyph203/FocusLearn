package model;

/**
 *
 * @author Admin
 */
public class Certificate {
    private int id;
    private String img;

    public Certificate() {
    }

    public Certificate(int id, String img) {
        this.id = id;
        this.img = img;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImg() {
        return img;
    }

    public void setImg(String img) {
        this.img = img;
    }
    
}
